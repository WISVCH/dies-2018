                          <td><p>De Beermile is een openactiviteit   die plaats zal vinden op het oude Bouwkundeterrein. De Beermile is een   race waar elke deelnemer bij de start een biertje moet adten en vervolgens   een rondje van minder dan  400m rent, weer een biertje adt en een rondje rent,   en zo door, tot dat je in totaal vier keer hebt gedaan.   De persoon die dit het snelst doet is de winnaar.&nbsp;</p>
                            <p>Klinkt simpel? Je moet je nog   wel aan de volgende spelregels zien te houden: deelnemers moeten hun   biertje eerst helemaal opgedronken hebben voordat ze (weer) mogen beginnen   met rennen. Afwijken van het parcours is niet toegestaan, en wordt bestraft   met het rennen van een extra rondje, net als brokken op een niet daarvoor   bestemde plek.&nbsp;</p>
                            <p>Deelname aan de Beermile kost   3 euro, je kunt je inschrijven bij de inschrijfbalie in de buurt van   het parcours of op CH, waarbij je ook een consumptiebon uitgereikt krijgt   voor patat. Deze bon kun je later inleveren bij de Ontsnappingslounge later   op diee avond. Indien er genoeg animo is zullen er meerdere rondes plaatsvinden.</p>
