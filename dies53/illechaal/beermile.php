                      <h2><strong>Beermile</strong></h2>
                      <table width="100%" border="0">
                        <tr>
                          <td><img src="images/run.jpg" width="98" height="119" /></td>
                          <td><p>De Beermile is een openactiviteit   die plaats zal vinden op het oude Bouwkundeterrein. De Beermile is een   race waar elke deelnemer bij de start een biertje moet adten en vervolgens   een rondje van ca. 400m rent, weer een biertje adt en een rondje rent,   en zo door, tot dat je in totaal 1600 meter (een mijl) hebt gerend.   De persoon die dit het snelst doet is de winnaar.&nbsp;<br />
                          </p>
                            <p>Klinkt simpel? Je moet je nog   wel aan de volgende spelregels zien te houden: deelnemers moeten hun   biertje eerst helemaal opgedronken hebben voordat ze (weer) mogen beginnen   met rennen. Afwijken van het parcours is niet toegestaan, en wordt bestraft   met het rennen van een extra rondje, net als brokken op een niet daarvoor   bestemde plek.&nbsp;<br />
                            </p>
                            <p>Deelname aan de Beermile kost   2 euro, je kunt je inschrijven bij de inschrijfbalie in de buurt van   het parcours of op CH, waarbij je ook een consumptiebon uitgereikt krijgt   voor patat. Deze bon kun je later inleveren bij de Ontsnappingslounge later   op diee avond. Indien er genoeg animo is zullen er meerdere rondes plaatsvinden.&nbsp;<br />
                            </p>
                            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;15.45   verzamelen/inschrijven deelnemers</p>
                            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;16.15   start 1e ronde Beermile</p>
                            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;16.45   start 2e ronde Beermile (bij genoeg animo)</p>
                          <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;19.00   prijsuitreiking (tijdens de Ontsnappingslounge)</p></td>
